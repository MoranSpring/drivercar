            <h2 class="headline">习近平会见哈佛校长 介绍两会情况</h2><br/>
            <div>2013.2.3</div>
            <div>来源：中国新闻网</div><br/>
            <img  src="http://cn.bing.com/az/hprichv/LondonTrainStation_GettyRR_139321755_ZH-CN742316019.jpg"/>  <br/><br/>           
            <p>习近平欢迎福斯特校长访华，并向福斯特简要介绍了刚刚闭幕的两会情况。他强调，我们提出了“两个一百年”的奋斗目标，为了实现这一目标，中国将走一条适合中国国情的可持续发展道路。去年我与奥巴马总统在北京成功会晤，进一步明确了推进中美新型大国关系建设的重点方向。我将于今年9月对美国进行国事访问，愿届时与美各界人士共商构建中美新型大国关系大计。人文交流在中美关系发展历程中发挥着积极作用，已成为新时期两国关系的重要支柱。希望双方共同努力，促进中美人文交流合作不断深化，为中美关系发展注入更多正能量。但不购买套餐者，自由购买学时训练。另外会员也可不通过平台报名考试而仅仅买学时练车。</p>
            <p>习近平指出，中国一直把教育放在优先发展的战略地位。中方重视同美方开展教育、科技交流合作。哈佛大学是世界知名高校，同中国教育界、科技界有着长期合作关系，取得了很好成果。希望两国高校增进交流，深化合作，更好服务于中美关系的发展。</p>
            <p>福斯特感谢习近平主席的会见。她表示，美中教育交流合作对增进两国人民了解，推动两国关系发展起到了积极作用。哈佛大学与中国已建立起长期的重要合作关系，愿继续加强同中国教育界和科技界的交流合作，为进一步促进美中关系发展作出贡献。</p>
